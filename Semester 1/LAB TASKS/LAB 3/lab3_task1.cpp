#include <iostream>
using namespace std;
int main()
{
    int x = 17;//already given number
    if (x % 2 == 0)
    {
        cout << "even";//if number gives reminder 0 then number is even
    }

    else
    {
        cout << "odd";//if number does not gives reminder 0 then number is odd
    }

    return 0;
}